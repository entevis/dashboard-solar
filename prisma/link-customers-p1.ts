async function main() {
  const lines = RAW.trim().split("\n");

  // 1. Collect unique customers by RUT
  const customerMap = new Map<string, { name: string }>();
  for (const line of lines) {
    const [, rut, , razonSocial] = line.split("\t");
    if (!customerMap.has(rut.trim())) {
      customerMap.set(rut.trim(), { name: razonSocial.trim() });
    }
  }

  // 2. Upsert customers
  console.log(`Upserting ${customerMap.size} unique customers...`);
  const rutToId = new Map<string, number>();
  for (const [rut, { name }] of customerMap) {
    const customer = await prisma.customer.upsert({
      where: { rut },
      update: { name },
      create: { rut, name },
    });
    rutToId.set(rut, customer.id);
  }
  console.log(`✓ ${customerMap.size} customers upserted`);

  // 3. Link plants to customers by solcorId
  let linked = 0;
  let notFound = 0;
  for (const line of lines) {
    const [solcorId, rut] = line.split("\t");
    const customerId = rutToId.get(rut.trim());
    if (!customerId) continue;

    const result = await prisma.powerPlant.updateMany({
      where: { solcorId: solcorId.trim(), portfolioId: 1 },
      data: { customerId },
    });

    if (result.count > 0) {
      linked += result.count;
    } else {
      console.warn(`  ⚠ Not found: solcorId="${solcorId.trim()}"`);
      notFound++;
    }
  }

  console.log(`✓ ${linked} plants linked to customers (${notFound} not found)`);
  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  prisma.$disconnect();
  process.exit(1);
});
